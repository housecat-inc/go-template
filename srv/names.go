func GenerateName() string {
	adj := catAdjs[rand.IntN(len(catAdjs))]
	noun := catNouns[rand.IntN(len(catNouns))]
	return adj + "-" + noun
}

func (s *Server) HandleGenerateName(c echo.Context) error {
	type Out struct {
		Name string `json:"name"`
	}
	return c.JSON(http.StatusOK, Out{Name: GenerateName()})
}
